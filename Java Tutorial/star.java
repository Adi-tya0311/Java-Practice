public class star{
    public static void main(String[] args){
        int i = 1;
        while(i <= 5){
            int j = 1;
            while(j <= i){
                System.out.print("*");
                j++;
            }
        System.out.println("");
        i++;
        }

        int x = 4;
        while(x >= 0){
            int y = 1;
            while(y <= x){
                System.out.print("*");
                y++;
            }
        System.out.println("");
        x--;
        }
    }
}
